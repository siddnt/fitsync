describe('API documentation endpoints', () => {
  it('serves the OpenAPI document', async () => {
    const res = await request(app).get('/api/docs/openapi.json');

    expect(res.status).toBe(200);
    expect(res.body?.openapi).toBe('3.1.0');
    expectedPaths.forEach((pathKey) => {
      expect(res.body?.paths?.[pathKey]).toBeDefined();
    });
    expect(res.body?.paths?.['/api/admin/audit-logs/export']?.get?.responses?.['200']?.content?.['text/csv']).toBeDefined();
    expect(res.body?.paths?.['/api/marketplace/webhook/stripe']?.post).toBeDefined();
  });

  it('serves the Swagger UI shell', async () => {
    const res = await request(app).get('/api/docs');

    expect(res.status).toBe(200);
    expect(res.text).toContain('SwaggerUIBundle');
    expect(res.text).toContain('/api/docs/openapi.json');
  });
});
